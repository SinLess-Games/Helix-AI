/**
 * @enum Pronoun
 * @description
 * A pronoun set reflects how a person refers to themselves in the third person.
 */
export enum Pronoun {
  /** He/Him pronouns */
  HeHim = 'He/Him',

  /** She/Her pronouns */
  SheHer = 'She/Her',

  /** They/Them pronouns */
  TheyThem = 'They/Them',

  /** Other or custom pronoun set */
  Other = 'Other',
}
